prvni_cislo = int(input("Zadej první číslo: "))
druhe_cislo = int(input("Zadej druhé číslo: "))
soucet = prvni_cislo + druhe_cislo
rozdil = prvni_cislo - druhe_cislo
soucin = prvni_cislo * druhe_cislo
podil = prvni_cislo / druhe_cislo
print(f"{prvni_cislo} + {druhe_cislo} = {soucet}")
print(f"{prvni_cislo} - {druhe_cislo} = {rozdil}")
print(f"{prvni_cislo} * {druhe_cislo} = {soucin}")
print(f"{prvni_cislo} / {druhe_cislo} = {podil}")
